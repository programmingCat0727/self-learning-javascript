
//基本變數類型

let myName = "墮貓"  // string 字串
let host = "萌貓"
let word = myName + host  //連接字串
console.log(myName);
console.log(word);


let score = 100  // number 數字
// + - * / %
console.log(score);

console.log(score + 100);
console.log(score - 200);
console.log(score * 50);
console.log(score / 200);
console.log(score % 5); //取餘數
console.log(score % 3);


let isFake = false // ture false boolean 布林
console.log(isFake);

let bag = undefined
console.log(bag);

let bag2 = null
console.log(bag2);


// let var const 宣告變數的指令<常使用 let const>
let a1 = "Aa"
console.log(a1);
a1 = "Nope" //修改變數
console.log(a1);

const b1 = "Bb"
console.log(b1);
